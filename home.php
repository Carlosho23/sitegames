<h2>A Saúde Pública</h2>
    <img src="img/saude-2.jpg" >
    <h5>Fonte:Criptomoedas</h5>
<br><br>
    
<p>O sistema de saúde brasileiro apresenta peculiaridades relacionadas à forma como o arranjo
público-privado se traduz na atenção à saúde. A atenção hospitalar é majoritariamente
realizada por entidades privadas, que podem simultaneamente atender pacientes financiados
pelo Sistema Único de Saúde (SUS) e pelos planos privados de saúde, produzindo arranjos
de financiamento e redes assistenciais complexos que dificultam a regulação do sistema. 
<br>
 A convivência do SUS com os planos de saúde tem sido considerada um fator gerador de desigualdades no acesso, utilização e qualidade dos serviços de saúde. Além disso, a concorrência pública-privada pela mesma rede de prestadores, sem separação da clientela atendida, gera problemas na organização do cuidado e prejudica a relação de complementariedade esperada para o sistema.
<br>
Ao longo dessas últimas décadas, observam-se diferentes institucionalidades da política social que evidenciam menor ou maior centralidade da área social e do papel do Estado nas estratégias de desenvolvimento. Do ponto de vista macroeconômico, a ênfase na estabilidade fiscal e monetária comprometeu maiores avanços na implantação de políticas de corte universal pelas restrições impostas à intervenção estatal e ao gasto social.
<br>
Acrescente-se que parte expressiva da força de trabalho em saúde - especialmente dos médicos - atua nos setores público e privado, em arranjos variados, sugerindo o seu imbrica mento e riscos de conflitos de interesses. Assim, enquanto parte das desigualdades na oferta de serviços e nos resultados sanitários é atenuada pela expansão de serviços do SUS - especialmente na atenção básica - o dinamismo do setor privado, sob incentivo estatal, tende a reproduzir a estratificação social e a expressão das desigualdades na saúde, sendo ainda susceptível aos ciclos econômicos.
</p>
