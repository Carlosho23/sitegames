<h2>Introdução sob Saúde</h2>
    <br>
    <img src="img/saude-1.jpg" width="1250px" height="250px">
    <h5>Fonte: Porto Seguro</h5>
    
<br>
    
<p>A relação entre arranjos organizacionais, envolvendo aspectos gerenciais, financeiros e estruturais, e sua influência sobre o funcionamento e desempenho das instituições de saúde tem sido amplamente discutida. Desse debate, emergem tipologias para a classificação dos estabelecimentos e análises comparativas. A diversidade de arranjos organizacionais, especialmente em termos financeiros e gerenciais, é maior entre as instituições hospitalares (HARDING; PREKER, 2003; JAKAB et al., 2002; LA FORGIA; COUTTOLENE, 2009). Na rede de atenção à saúde brasileira, tal diversidade é visível, contudo há particularidades, sobretudo no que diz respeito às fontes de pagamento constituintes dos arranjos de financiamento dos serviços. Não é exceção pacientes utilizarem simultaneamente serviços financiados pelo Sistema Único de Saúde (SUS) e por planos privados de saúde; muitos profissionais de saúde têm mais de um emprego remunerados por essas duas fontes (VICTORA et al., 2011). Além disso, há instituições públicas e privadas prestando cuidado a pacientes financiados pelas duas fontes de pagamento. Embora ainda sejam raros os estudos, estão presentes na agenda sanitária preocupações com a variação no padrão de cuidado prestado aos dois grupos, com destaque aos problemas de acesso, efetividade e equidade decorrentes da fonte e valor do reembolso (VICTORA et al., 2011).
    
<br><br>
    
À complexidade do sistema de saúde, soma-se aquela concernente à gestão dos hospitais, pois nestes estão concentrados grande parte dos recursos financeiros, materiais, tecnológicos e humanos. Apesar disso, diferentemente de outros setores da economia regidos pela relação custo-volume-lucro, em geral a gestão prevalente nos hospitais é a do regime de caixa (VECINA-NETO; MALIK, 2007). Embora esta não comprometa a busca por lucratividade, aponta a complexidade da gestão financeira dessas instituições. A diversificação das fontes de financiamento tem sido adotada por instituições hospitalares no Brasil com o intuito de amenizar o risco de endividamento decorrente de eventual insuficiência em uma das fontes de pagamento (CARVALHO, 2007).
</p>
    
