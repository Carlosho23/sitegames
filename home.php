<h2>Saúde Privada</h2>
    <img src="img/saude-3.jpg" width="700px" height="400px">
    <h5>Fonte:Dinheirovivo</h5>
<br>
<p>O setor privado de saúde cresceu muito no período militar, nas décadas de 1960 a 1980, e consolidou definitivamente sua importância nos últimos vinte anos. O mercado da indústria brasileira de planos de saúde é composto hoje por: cooperativa médica, medicina de grupo, seguradora especializada em saúde e filantropia.
<br>
A contratação de assistência médica suplementar oferece vantagens para as empresas e para os afiliados. As empresas incluem a assistência médica em suas políticas de recursos humanos e nas pautas de negociação salarial, onde a assistência médica funciona como moeda de troca, especialmente em contextos de salários congelados – entrando como salário indireto, ao proporcionar isenção de encargos, e também como despesa operacional, para fins de imposto de renda e repasse para preços.
<br>
Para os afiliados, a assistência médica suplementar é uma alternativa ao alto preço da medicina privada, garante o atendimento a risco futuro de enfermidade de alto custo, evita precisar recorrer ao serviço público de saúde e permite aproveitar o incentivo do subsídio dado pelo imposto de renda.
<br>
As realidades mais recentes da medicina privada do Brasil foram bastante alteradas pela Constituição de 1988, pelo Código de Defesa do Consumidor e pela Lei 9.656 de 1998, que regulamentou o setor de medicina suplementar.
No presente quadro de retração do setor, nenhum dos atores da cadeia quer ter sua liberdade de escolha ou de trabalho limitada. O setor está engessado e sem perspectiva alguma de crescimento. Empresários, clientes, médicos, laboratórios e hospitais estão insatisfeitos tanto com o que é cobrado quanto com o que é pago. Além disso, os controles exagerados e a baixa rentabilidade desestimulam os investimentos no setor.
</p>

